public class Ponto2D {
    private double x, y;

    public Ponto2D(){
        x = 0;
        y = 0;
    }

    public Ponto2D(double x, double y){
        this.x = x;
        this.y = y;
    }

    public Double getX(){
        return x;
    }

    public Double getY(){
        return y;
    }

}
