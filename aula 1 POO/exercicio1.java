public class exercicio1 {
    public static void main(String[] args) {
        int n = 10;
        while(n <= 25){
            System.out.println(n);
            n++;
        }

        n = 10;

        do{
            System.out.println(n);
            n++;
        }while(n <= 25);

        for(n = 10; n <= 25; n++){
            System.out.println(n);
        }
    }
}